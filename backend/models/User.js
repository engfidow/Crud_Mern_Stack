const mongoose = require('mongoose');

const UserSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    image: String // assuming URL to the image
}, {
    timestamps: true
});

module.exports = mongoose.model('User', UserSchema);
